def draw_char(buffer, char: str, x: int, y: int, color: Tuple[int, int, int],
              font: Dict[str, List[List[int]]] = None):
    """Draw a single character using a 3x5 pixel font.

    Args:
        buffer: PixelBuffer to draw on.
        char: Character to draw.
        x, y: Top-left coordinate.
        color: RGB color tuple.
        font: Font dict to use (default: FONT_THIN).
    """
    if font is None:
        font = FONT_THIN
    pattern = font.get(char.upper(), font.get(' ', [[0]*3]*5))

    for py in range(5):
        for px in range(3):
            if pattern[py][px]:
                buffer.set_pixel(x + px, y + py, color)


def draw_text(buffer, text: str, x: int, y: int, color: Tuple[int, int, int],
              center: bool = False, char_spacing: int = 4,
              font: Dict[str, List[List[int]]] = None):
    """Draw text using a pixel font.

    Args:
        buffer: PixelBuffer to draw on.
        text: Text string to draw.
        x: X coordinate (top-left, or center if center=True).
        y: Y coordinate (top-left).
        color: RGB color tuple.
        center: If True, center the text at x coordinate.
        char_spacing: Spacing between characters (default 4 = 3px char + 1px gap).
        font: Font dict to use (default: FONT_THIN).
    """
    if center:
        text_width = get_text_width(text, char_spacing)
        x = x - text_width // 2

    for i, char in enumerate(text):
        draw_char(buffer, char, x + i * char_spacing, y, color, font=font)


def get_text_width(text: str, char_spacing: int = 4) -> int:
    """Get the width in pixels of a text string."""
    if not text:
        return 0
    return len(text) * char_spacing - 1
